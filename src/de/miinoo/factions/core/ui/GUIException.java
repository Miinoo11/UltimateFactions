package de.miinoo.factions.core.ui;

/**
 * @author DotClass
 *
 */
public class GUIException extends UIException {

	private static final long serialVersionUID = -9197687597916920822L;

	public GUIException() {
		super();
	}

	public GUIException(String message, Throwable cause) {
		super(message, cause);
	}

	public GUIException(String message) {
		super(message);
	}

	public GUIException(Throwable cause) {
		super(cause);
	}

}
